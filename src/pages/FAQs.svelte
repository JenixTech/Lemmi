  <h3>1. Using Lemmi</h3>
  <details>
    <summary>How does Lemmi work?</summary>
    <div class="details-wrapper">
      <p>Choose words or phrases from a range of different Categories.</p>
      <p>Selected words and phrases will appear in the Speech Bar at the top of the screen. By default, Lemmi will read this aloud right away.</p>
      <p>To repeat the selected text tap the ‘Play’ button beneath the Speech Bar.</p>
    </div>
  </details>
  <details>
    <summary>What do the Speech Bar buttons do?</summary>
    <div class="details-wrapper">
      <div class="iconed-text">
