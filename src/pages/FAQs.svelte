        <p>Clear: Clears the text in the Speech Bar</p>
      </div>
      <div class="iconed-text">
        <img src="images/undo.png" alt="undo icon"/>
        <p>Undo: Undo clears the last text that was added to the Speech Bar</p>
      </div>
      <div class="iconed-text">
        <img src="images/play.png" alt="play icon"/>
        <p>Play: Plays the text in the Speech Bar</p>
      </div>
    </div>
  </details>
