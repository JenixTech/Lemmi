<section class="faq-section">
  <h3>2. Customisation</h3>
  <details>
    <summary>How do I add custom entry words and images?</summary>
    <div class="details-wrapper">
      <div class="iconed-text">
        <img src="images/dictionary.png" alt="dictionary icon" />
        <p>Tap on the Dictionary button.</p>
      </div>
      <div class="iconed-text">
        <img src="images/add.png" alt="add entry icon" />
        <div class="iconed-text-wrapper">
          <p>Tap 'Add Entry'.</p>
          <p>On this new screen, enter your text, select the category you want your entry to be stored in, and, optionally, choose an image.</p>
          <p>Tap 'Save'.</p>
        </div>
      </div>
      <p>Note: You mustupload content that is yours, or that have been granted the right to use. For more information please see our <a href="/terms-of-service.html">Terms of Service.</a></p>
    </div>
  </details>
  <details>
    <summary>How do I edit or delete custom entry words and images?</summary>
    <div class="details-wrapper">
      <div class="iconed-text">
        <img src="images/dictionary.png" alt="dictionary icon" />
        <p>Tap on the Dictionary button.</p>
      </div>
      <div class="iconed-text">
        <img src="images/word-book.png" alt="word book icon" />
        <div class="iconed-text-wrapper">
          <p>Tap on the Word Book button.</p>
          <p>To edit: tap 'Edit' on the entry you want to change. Update the text, category or image, and tap to confirm.</p>
          <p>To delete: tap 'Delete' on the entry you want to delete and tap to confirm.</p>
        </div>
      </div>
    </div>
  </details>
  <details>
    <summary>Are my custom entries safe?</summary>
    <div class="details-wrapper">
      <p>Yes, your custom entries are stored to your personal account. We do not have access to this, nor do we collect or store any of this information.</p>
      <p>Note: for Android users, or those using Lemmi without syncing to iCloud, you will lose any custom data if you delete the app from your device.</p>
    </div>
  </details>
  <details>
    <summary>How do I change the speaking voice?</summary>
    <div class="details-wrapper">
      <div class="iconed-text">
        <img src="images/settings.png" alt="settings icon" />
        <div class="iconed-text-wrapper">
          <p>Tap on the Settings button.</p>
          <p>Tap on the voice dropdown menu and tap to select a voice. </p>
          <p>To hear a sample voice please tap the 'Play'.</p>
        </div>
      </div>
    </div>
  </details>
  <details>
    <summary>How do I add alternative voices?</summary>
    <div class="details-wrapper">
      <p>For iOS: Go to your device's Settings menu. Select 'Accessibility', then 'Spoken Content' and tap 'Voices'. We recommend using voices marked 'Enhanced'.</p>
      <p>For Android: Go to your device's Settings. Select Accessibility, then tap the 'Preferred Engine Settings' symbol and tap 'Install Voice Data'.</p>
    </div>
  </details>
  <details>
    <summary>How do I change the app's colour scheme?</summary>
    <div class="details-wrapper">
      <div class="iconed-text">
        <img src="images/settings.png" alt="settings icon" />
        <div class="iconed-text-wrapper">
          <p>Tap on the Settings button, and choose your preferred theme.</p>
          <p>For iOS: toggle 'Dark Mode' on or off.</p>
          <p>For Android: choose between 'Light', 'Dark', and 'Default' modes. Selecting 'Default' will tell Lemmi to follow your device's settings.</p>
        </div>
      </div>
    </div>
  </details>
  <details>
    <summary>How do I turn off the Auto Speak option?</summary>
    <div class="details-wrapper">
      <div class="iconed-text">
        <img src="images/settings.png" alt="settings icon" />
        <p>Tap on the Settings button, then turn ‘Auto Speak’ off.</p>
      </div>
    </div>
  </details>
  <details>
    <summary>How do I hide Suggested Words?</summary>
    <div class="details-wrapper">
      <div class="iconed-text">
        <img src="images/settings.png" alt="settings icon" />
        <p>Tap on the Settings button, then turn ‘Show Suggested Words’ off.</p>
      </div>
    </div>
  </details>
  <details>
    <summary>How do I hide Images and just have text?</summary>
    <div class="details-wrapper">
      <div class="iconed-text">
        <img src="images/settings.png" alt="settings icon" />
        <p>Tap on the Settings button, then turn ‘Show Images' off.</p>
      </div>
    </div>
  </details>
